/** True when the domain (or a parent domain) is a known disposable host. */
function domainIsListed(domain: string): boolean {
  const parts = domain.split('.');
  // Check domain and every parent suffix (a.b.mailinator.com → mailinator.com, …)
  for (let i = 0; i < parts.length - 1; i++) {
    const candidate = parts.slice(i).join('.');
    if (DISPOSABLE_DOMAINS.has(candidate)) return true;
  }
  return false;
}

function domainMatchesPattern(domain: string): boolean {
  return DISPOSABLE_DOMAIN_PATTERNS.some((re) => re.test(domain));
}

/**
 * Returns true if this address is almost certainly a temporary / disposable inbox.
 * Free, offline, no external API.
 */
export function isDisposableEmail(email: string): boolean {
  const domain = emailDomain(email);
  if (!domain) return false;
  if (domainIsListed(domain)) return true;
  if (domainMatchesPattern(domain)) return true;
  return false;
}

