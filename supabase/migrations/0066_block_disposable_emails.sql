-- Reject disposable emails on auth.users insert/update (cannot be bypassed from clients).
create or replace function public.reject_disposable_auth_email()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if public.is_disposable_email(new.email) then
    raise exception 'disposable_email_not_allowed'
      using errcode = 'P0001',
            hint = 'Use a permanent email address.';
  end if;
  return new;
end;
$$;

drop trigger if exists trg_reject_disposable_auth_email_ins on auth.users;
create trigger trg_reject_disposable_auth_email_ins
  before insert on auth.users
