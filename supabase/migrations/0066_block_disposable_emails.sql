-- True when email uses a disposable domain (exact or subdomain of a listed host).
create or replace function public.is_disposable_email(p_email text)
returns boolean
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  v_email text;
  v_domain text;
  v_parts text[];
  v_i int;
  v_candidate text;
begin
  v_email := lower(btrim(coalesce(p_email, '')));
  if v_email = '' or position('@' in v_email) < 2 then
    return false;
  end if;

  v_domain := split_part(v_email, '@', 2);
  v_domain := trim(both '.' from regexp_replace(v_domain, '\.+', '.', 'g'));
  if v_domain = '' or position('.' in v_domain) = 0 then
    return false;
  end if;

  -- Exact + parent-suffix match (a.b.mailinator.com → mailinator.com)
  v_parts := string_to_array(v_domain, '.');
  for v_i in 1 .. (array_length(v_parts, 1) - 1) loop
    v_candidate := array_to_string(v_parts[v_i : array_length(v_parts, 1)], '.');
    if exists (
      select 1 from public.disposable_email_domains d where d.domain = v_candidate
    ) then
      return true;
    end if;
  end loop;

  -- Brand-pattern catch-all for common temp-mail families (still free / offline).
  if v_domain ~* '(^|\.)(temp-?mail|tempmail|tmpmail|tmpeml|tmpbox|mailinator|yopmail|guerrill?a-?mail|sharklasers|maildrop|getnada|mailnesia|discard(mail)?|dispostable|fakeinbox|fakemail|emailfake|tempail|tempinbox|mintemail|mytemp\.?email|tempmailo|crazymailing|mailpoof|inboxkitten|burnermail|emailondeck|mailnull|spamgourmet|mailforspam|minuteinbox|mail-temp|tempr\.email|tempmailer|mailtemp|moakt|mohmal|1secmail|dropmail\.me|mail\.tm|mail\.gw|jetable|spamfree24|willselfdestruct|selfdestructingmail|mailsac|(10|15|20)minut?e?s?-?mail|throw-?away|trash-?mail|trashy?mail)([.-]|$)'
     or v_domain ~* '(^|\.)(spam4\.me|gustr\.com|dayrep\.com|einrot\.(com|de)|teleworm\.(com|us)|armyspy\.com|cuvox\.de|rhyta\.com|jourrapide\.com|superrito\.com)$'
  then
    return true;
  end if;

  return false;
end;
$$;

