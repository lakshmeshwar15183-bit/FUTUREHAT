/** Search catalog by English keyword (WhatsApp-style). */
export function searchEmojis(query: string, limit = 120): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const hits: string[] = [];
  const seen = new Set<string>();
  const add = (e: string) => {
    if (!seen.has(e) && e) {
      seen.add(e);
      hits.push(e);
    }
  };

  for (const [key, pack] of Object.entries(KEYWORD_PACKS)) {
    if (key.includes(q) || q.includes(key)) {
      pack.split(/\s+/).forEach(add);
    }
  }

  // Category labels
  for (const cat of EMOJI_CATEGORIES) {
    if (cat.label.toLowerCase().includes(q) || cat.id.includes(q)) {
      cat.emojis.slice(0, 40).forEach(add);
    }
  }

  // Direct glyph paste / partial
  if (q.length <= 4) {
    for (const e of ALL_EMOJIS) {
      if (e.includes(query.trim())) add(e);
      if (hits.length >= limit) break;
    }
  }

  return hits.slice(0, limit);
}
